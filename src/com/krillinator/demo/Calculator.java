package com.krillinator.demo;

public class Calculator {

    public int addition(int personNumber, int age) {

        System.out.println(personNumber + age);

        return personNumber + age;
    }

    public int subtraction(int personNumber, int age) {

        System.out.println(personNumber - age);

        return personNumber - age;

    }

    public int multiplic(int personNumber, int age) {

        System.out.println(personNumber * age);

        return personNumber * age;
    }

    public int divis(int personNumber, int age) {

        System.out.println(personNumber / age);

        return personNumber / age;
    }

}
